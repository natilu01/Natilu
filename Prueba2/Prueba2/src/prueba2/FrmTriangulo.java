    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        if(cmbTriangulo.getSelectedItem().equals("Rectangulo")){
            a = Double.valueOf(txtLadoA.getText());
            b = Double.valueOf(txtLadoB.getText());
            c = Math.sqrt(Math.pow(a,2)+Math.pow(b,2));
            //System.out.println(a +" "+b+" "+c);
            TrianguloRectangulo t = new TrianguloRectangulo(a, b, c);
            if(t.validar() == true){
                lblArea.setText(String.valueOf(t.area()));
                lblPerimetro.setText(String.valueOf(t.perimetro()));
                lblAltura.setText(String.valueOf(t.altura()));
                //System.out.println("si es un triangulo");
            }else{
                txtLadoA.setText(null);
                txtLadoB.setText(null);
            }
            
        }else if(cmbTriangulo.getSelectedItem().equals("Isosceles")){
            a = Double.valueOf(txtLadoA.getText());
            b = Double.valueOf(txtLadoB.getText());
            c = a;
            //System.out.println(a +" "+b+" "+c);
            TrianguloIsosceles t = new TrianguloIsosceles(a, b, c);
            if(t.validar() == true){
                lblArea.setText(String.valueOf(t.area()));
                lblPerimetro.setText(String.valueOf(t.perimetro()));
                lblAltura.setText(String.valueOf(t.altura()));
                //System.out.println("si es un triangulo");
            }else{
                txtLadoA.setText(null);
                txtLadoB.setText(null);
            }
        }else if(cmbTriangulo.getSelectedItem().equals("Escaleno")){
            a = Double.valueOf(txtLadoA.getText());
            b = Double.valueOf(txtLadoB.getText());
            c = Double.valueOf(txtLadoC.getText());
            if(a == b || a == c || b == c ){
                JOptionPane.showMessageDialog(null, "Los Tres valores deben de ser diferentes", "ERROR", JOptionPane.WARNING_MESSAGE);
            }else{
                //System.out.println(a +" "+b+" "+c);
                TrianguloEscaleno t = new TrianguloEscaleno(a, b, c);
                if(t.validar() == true){
                    lblArea.setText(String.valueOf(t.area()));
                    lblPerimetro.setText(String.valueOf(t.perimetro()));
                    lblAltura.setText(String.valueOf(t.altura()));
                    //System.out.println("si es un triangulo");
                }else{
                    txtLadoA.setText(null);
                    txtLadoB.setText(null);
                }
            }
        }else if(cmbTriangulo.getSelectedItem().equals("Equilatero")){
            a = Double.valueOf(txtLadoA.getText());
            b = a;
            c = a;
            //System.out.println(a +" "+b+" "+c);
            TrianguloEquilatero t = new TrianguloEquilatero(a, b, c);
            if(t.validar() == true){
                lblArea.setText(String.valueOf(t.area()));
                lblPerimetro.setText(String.valueOf(t.perimetro()));
                lblAltura.setText(String.valueOf(t.altura()));
                //System.out.println("si es un triangulo");
            }else{
                txtLadoA.setText(null);
                txtLadoB.setText(null);
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

